const Sapna = () => {
  const [searchTerm, setSearchTerm] = useState('');
  const [searchResults, setSearchResults] = useState([]);

  const handleSearch = () => {
    const caseExp = new RegExp(searchTerm, 'i');
    const filteredResults = [];

    data.forEach(obj => {
      for (let key in obj) {
        if (caseExp.test(key) || caseExp.test(obj[key])) {
          filteredResults.push({ key, value: obj[key] });
        }
      }
    });

    setSearchResults(filteredResults);
  };

  useEffect(() => {
    if (searchTerm === '') {
      setSearchResults([]);
      return; // Exit early if search term is empty
    }
    handleSearch();
  }, [searchTerm]);

  const handleResultClick = (result) => {
    speak(result);
  };

  const speak = (result) => {
    const text = `${result.key}. ${result.value}`;
    Speech.speak(text); // Use expo-speech to speak the result
  };

  return (
    <>
    <Siri />
    <View style={styles.container}>
      <Text style={styles.header}>What is a dream?</Text>
      <TextInput
        style={styles.textInput}
        placeholder="Enter your dream"
        value={searchTerm}
        onChangeText={setSearchTerm}
      />
      
      <ScrollView style={styles.resultsContainer}>
        {searchResults.length > 0 ? (
          <View style={styles.resultGrid}>
            {searchResults.map((val, idx) => (
              <TouchableOpacity key={idx} style={styles.resultCard} onPress={() => handleResultClick(val)}>
                <Text style={styles.resultKey}>{val.key}</Text>
                <Text style={styles.resultValue}>{val.value}</Text>
              </TouchableOpacity>
            ))}
          </View>
        ) : searchTerm && (
          <View style={styles.noResultContainer}>
            <Text style={styles.noResultText}>No Result!</Text>
            <Text style={styles.noResultSubtext}>
              Sorry, your search query returned no results.
            </Text>
          </View>
        )}
      </ScrollView>
    </View>
    <CustomBottomNavbar />
    </>
  );
}

